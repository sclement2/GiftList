app.post("/gift", (req, res) => {
  // grab the parameters from the front-end here
  const body = req.body;
  /* 
  expects to receive the name to be verified and a proof generated by the client
  format provided is an array with two entries 
  */

  let isInTheList = false;
  const giftList = ["robot", "airplane", "boat", "doll"]; // list of gifts
  const gift = giftList[Math.floor(Math.random() * 4)]; // select random gift

  // Takes the info from the client and the MERKLE_ROOT and verifies if the client provide name is in the "nice list" for a gift.
  isInTheList = verifyProof(body.proof, body.name, MERKLE_ROOT);

  if (isInTheList) {
    res.send(body.name + ", you got a toy " + gift + "!");
  } else {
    res.send(body.name + ", you are not on the list :(");
  }
});

app.listen(port, () => {
  console.log(`Listening on port ${port}!`);
});
